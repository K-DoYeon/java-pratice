package sec03_ch03;

public class Ch03Exam06 {

	public static void main(String[] args) {
		int apple = 1;
		int totalPiece = apple * 10;
		int number = 7;
		int temp = totalPiece - number;
		
		double result = temp / 10.0;
		
		System.out.println("사과 한 조각에서 7조각을 빼면");
		System.out.println(result + "조각이 남는다.");

	}

}
