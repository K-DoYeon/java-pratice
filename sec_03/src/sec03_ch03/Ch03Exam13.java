package sec03_ch03;

public class Ch03Exam13 {

	public static void main(String[] args) {
		int rs = 0;
		rs += 10;
		System.out.println(rs); //10 rs = rs +10

		rs -= 5;
		System.out.println(rs); //5 rs = rs - 5
		
		rs *= 3;
		System.out.println(rs); //15
		
		rs /= 5;
		System.out.println(rs); //3
		
		rs %= 3;
		System.out.println(rs); //0
	}

}
