package sec04_ch02;

public class If_exam01 {

	public static void main(String[] args) {
		int score = 89;
		if(score >= 90) {
			System.out.println("점수가 90이상 이네요.");
			System.out.println("등급은 A입니다.");
		}
		if(score < 90) {
			System.out.println("점수가 90미만 입니다.");
			System.out.println("A등급은 아니에요.");
		}

	}

}
